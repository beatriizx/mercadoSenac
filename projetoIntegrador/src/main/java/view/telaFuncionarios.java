     new Utilitarios().LimpaTela(pnlDadosPessoais);
     new Utilitarios().LimpaTela(pnlEndereco);
     new Utilitarios().LimpaTela(pnlContato);
     new Utilitarios().LimpaTela(Usuario);
     
     
       
       
