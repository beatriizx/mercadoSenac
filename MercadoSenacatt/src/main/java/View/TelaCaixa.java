    private void btnExcluirProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirProdutoActionPerformed
               int linhaSelecionada = tblCarrinho.getSelectedRow();
        if(linhaSelecionada == -1){
            JOptionPane.showMessageDialog(this, "Nenhum produto selecionado, tente novamente");
            
            
        } else{
             DefaultTableModel tabelaProduto = (DefaultTableModel) tblCarrinho.getModel();
             tabelaProduto.removeRow(linhaSelecionada);
              JOptionPane.showMessageDialog(this, "Produto Excluido");
            
        }
    }//GEN-LAST:event_btnExcluirProdutoActionPerformed

